package tcs.model;

public class DataCoordenada {
    private int EjeX;
    private int EjeY;
    public DataCoordenada(int ejeX, int ejeY) {
        EjeX = ejeX;
        EjeY = ejeY;
    }

    public int getEjeX() {return EjeX;}
    public void setEjeX(int ejeX) {EjeX = ejeX;}

    public int getEjeY() {return EjeY;}
    public void setEjeY(int ejeY) {EjeY = ejeY;}



}
